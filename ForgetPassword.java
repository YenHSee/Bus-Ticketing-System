    private void jbtConfrimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtConfrimActionPerformed
      String userID=jtfUserID.getText();
        char[] passNew=jpwNew.getPassword();
     char[] passCon=jpwConfrim.getPassword();
     String password1="";  String password2="";
     Staff s =staffControl.selectRecord(userID);
       for(int i=0;i<passNew.length;i++){
           password1+=passNew[i];
             
       }
         for(int i=0;i<passCon.length;i++){
           password2+=passCon[i];
             
       }
         if(password1.equals(password2)){
             s.setPassword(password2);
             
             Staff staff=new Staff(s.getStaffID(),s.getStaffName(),s.getPhoneNumber(),s.getEmailAddress(),s.getStaffPosition(),s.getPassword(),s.getAddress(),s.getSecurityQuestion(),s.getSecurityAnswer(),s.getGender(),s.getAge(),s.getIcNumber(),s.getDateOfBirth(),s.getHomeContractNum());
         staffControl.updateRecord(staff);
          JOptionPane.showMessageDialog(null, "Password Updated.","Sucessful ",JOptionPane.INFORMATION_MESSAGE);
         }else{
              JOptionPane.showMessageDialog(null, "The new password is not match with the confrim password.","Wrong Infromation",JOptionPane.ERROR_MESSAGE);
         }
    }//GEN-LAST:event_jbtConfrimActionPerformed

