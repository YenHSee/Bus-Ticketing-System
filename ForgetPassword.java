    private void jbtSerachActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtSerachActionPerformed
      String userID=jtfUserID.getText();
      
      Staff s=staffControl.selectRecord(userID);
      
      if(s !=null){
          jtfQuestion.setEnabled(true);
          jtfQuestion.setText(s.getSecurityQuestion());
          jtfAnswer.setEnabled(true);
          jbtSubmit.setEnabled(true);
          
      }else{
          JOptionPane.showMessageDialog(null, "This user ID  not exists.","Record Not Found ",JOptionPane.ERROR_MESSAGE);
      }
      
    }//GEN-LAST:event_jbtSerachActionPerformed

